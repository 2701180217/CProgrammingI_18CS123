#include <iostream>

using namespace std;

int main() {
    cout << (4 > 3) << endl;  // 1
    cout << (4 < 3) << endl;  // 0
    cout << (4 >= 3) << endl;  // 1
    cout << (4 <= 3) << endl;  // 0
    cout << (4 == 3) << endl;  // 0
    cout << (4 != 3) << endl;  // 1

    system("PAUSE");
    return 0;
}
